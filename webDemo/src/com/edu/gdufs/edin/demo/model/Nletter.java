package com.edu.gdufs.edin.demo.model;

public class Nletter {
	
	private Integer _id;
	private String _nletter;
	private Integer _count;
	private Integer _nwordsCountId;
	
	public Integer getId() {
		return _id;
	}
	public void setId(Integer _id) {
		this._id = _id;
	}
	public String getNletter() {
		return _nletter;
	}
	public void setNletter(String _nletter) {
		this._nletter = _nletter;
	}
	public Integer getCount() {
		return _count;
	}
	public void setCount(Integer _count) {
		this._count = _count;
	}
	public Integer getNwordsCountId() {
		return _nwordsCountId;
	}
	public void setNwordsCountId(Integer _nwordsCountId) {
		this._nwordsCountId = _nwordsCountId;
	}
	
	

}
